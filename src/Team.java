public class Team {
	
	private String teamName;
	private String teamCountry;
	private String teamStadium;
	private String teamLeague;
	private int teamFoundationYear;
	
	public String getTeamName() {
		
		return teamName;
		
    }

	public void setTeamName(String TeamName) {
		
		this.teamName = TeamName;
		
	}

	public String getTeamCountry() {
		
		return teamCountry;
		
	}

	public void setTeamCountry(String TeamCountry) {
		
		this.teamCountry = TeamCountry;
		
	}
	
	public String getTeamStadium() {
		
		return teamStadium;
		
	}

	public void setTeamStadium(String TeamStadium) {
		
		this.teamStadium = TeamStadium;
		
	}

	public String getTeamLeague() {
		
		return teamLeague;
		
	}

	public void setTeamLeague(String TeamLeague) {
		
		this.teamLeague = TeamLeague;
		
	}

	public int getTeamFoundationYear() {
		
		return teamFoundationYear;
		
	}

	public void setTeamFoundationYear(int TeamFoundationYear) {
		
		this.teamFoundationYear = TeamFoundationYear;
		
	}
	
}